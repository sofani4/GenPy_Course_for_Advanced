# –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —Å–ª–æ–≤–æ YES, –µ—Å–ª–∏ –º–∞—Ç—Ä–∏—Ü–∞ —è–≤–ª—è–µ—Ç—Å—è –º–∞–≥–∏—á–µ—Å–∫–∏–º –∫–≤–∞–¥—Ä–∞—Ç–æ–º, –∏ —Å–ª–æ–≤–æ NO –≤ –ø—Ä–æ—Ç–∏–≤–Ω–æ–º —Å–ª—É—á–∞–µ.'
# –ú–ì - —Å—É–º–º—ã –ø–æ –∫–∞–∂–¥–æ–º—É —Å—Ç–æ–ª–±—Ü—É, –∫–∞–∂–¥–æ–π —Å—Ç—Ä–æ–∫–µ –∏ –∫–∞–∂–¥–æ–π –∏–∑ –¥–≤—É—Ö –¥–∏–∞–≥–æ–Ω–∞–ª–µ–π —Ä–∞–≤–Ω—ã –º–µ–∂–¥—É —Å–æ–±–æ–π


def check_magic_square(matrix, n):
    sum_compare = sum0 = sum1 = sum2 = 0
    n_sqr = n ** 2
    set_num = set()
    # —Ü–∏–∫–ª –¥–ª—è –¥–∏–∞–≥–æ–Ω–∞–ª–µ–π
    for i in range(n):
        elem1 = matrix[i][i]
        elem2 = matrix[i][n - i - 1]
        if 0 < elem1 <= n_sqr and 0 < elem2 <= n_sqr:
            set_num.update({elem1, elem2})
        else:
            return 'NO'
        sum_compare += elem1  # —Å—É–º–º–∞ –≥–ª–∞–≤–Ω–æ–π –¥–∏–∞–≥–æ–Ω–∞–ª–∏
        sum0 += elem2  # —Å—É–º–º–∞ –ø–æ–±–æ—á–Ω–æ–π –¥–∏–∞–≥–æ–Ω–∞–ª–∏
    if sum_compare != sum0:
        return 'NO'

    for i in range(n):
        for j in range(n):
            elem1 = matrix[i][j]
            elem2 = matrix[j][i]
            if 0 < elem1 <= n_sqr and 0 < elem2 <= n_sqr:
                set_num.update({elem1, elem2})
            else:
                return 'NO'
            sum1 += elem1  # —Å—É–º–º–∞ –ø–æ –≥–æ—Ä–∏–∑–æ–Ω—Ç–∞–ª–∏
            sum2 += elem2  # —Å—É–º–º–∞ –ø–æ –≤–µ—Ä—Ç–∏–∫–∞–ª–∏
        if sum_compare != sum1 or sum_compare != sum2:
            return 'NO'
        sum1 = sum2 = 0
    return 'YES' if len(set_num) == n ** 2 else 'NO'


n = int(input())
matrix = [[int(i) for i in input().split()] for _ in range(n)]
print(check_magic_square(matrix, n))
