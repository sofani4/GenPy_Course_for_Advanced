# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —É–∫–∞–∑–∞–Ω–Ω—ã–π –≤–ª–æ–∂–µ–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫.
# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –ù–µ –∑–∞–±—É–¥—å—Ç–µ –≤—ã–∑–≤–∞—Ç—å —Ñ—É–Ω–∫—Ü–∏—é chunked(), —á—Ç–æ–±—ã –≤—ã–≤–µ—Å—Ç–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç üòÄ.

def chunked(str, n):
    lst = []
    for i in range(0, len(str), n):
        if len(str[i:]) <= n:
            lst.append(str[i:])
        else:
            lst.append(str[i:i + n])
    print(lst)


str = input().split()
n = int(input())
chunked(str, n)
