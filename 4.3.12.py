# v1
lst_inp = [i for i in input().split()]
lst = []
tmp = [lst_inp[0]]

for i in range(1, len(lst_inp)):
    if lst_inp[i] == lst_inp[i - 1]:
        tmp += lst_inp[i]
    else:
        lst.append(tmp)
        tmp = [lst_inp[i]]
lst.append(tmp)
print(lst)

