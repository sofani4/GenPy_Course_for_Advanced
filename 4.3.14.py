# v1
def list_sublists(orig_lst):
    lst = [[]]
    amount = 0
    for i in range(1, len(orig_lst)):
        for j in range(len(orig_lst)):
            if len(orig_lst[j:j + i]) == i:
                lst.append(orig_lst[j:j + i])
            amount += 1
    lst.append(orig_lst)
    print(lst)


list_sublists(input().split())


