def list_sublists2(orig_lst):
    new_lst = [[]]
    for i in range(1, len(orig_lst) + 1):
        # –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏–µ –Ω–∞ j: –Ω–µ –ø—Ä–∏–≤—ã—à–∞—è –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã—Ö –≥—Ä–∞–Ω–∏—Ü
        for j in range(len(orig_lst) - i + 1):
            new_lst.append(orig_lst[j:j + i])
    print(new_lst)


